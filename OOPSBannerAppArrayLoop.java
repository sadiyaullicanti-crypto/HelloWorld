public class OOPSBannerAppArrayLoop {

    public static void main(String[] args) {

        String[] banner = {
                "***********************",
                "*                     *",
                "*      HELLO OOPS     *",
                "*                     *",
                "***********************"
        };

        for (int i = 0; i < banner.length; i++) {
            System.out.println(banner[i]);
        }
    }
}
